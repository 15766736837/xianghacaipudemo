public class FuLiaoBean {
    private String oneEdt;
    private String twoEdt;

    public FuLiaoBean(String oneEdt, String twoEdt) {
        this.oneEdt = oneEdt;
        this.twoEdt = twoEdt;
    }

    public String getOneEdt() {
        return oneEdt;
    }

    public void setOneEdt(String oneEdt) {
        this.oneEdt = oneEdt;
    }

    public String getTwoEdt() {
        return twoEdt;
    }

    public void setTwoEdt(String twoEdt) {
        this.twoEdt = twoEdt;
    }
}
